package com.tjp.domain;

public class Attention {
	private String focus_id;
	private String focus;
	private String befocus;
	public Attention() {
	}
	public String getFocus_id() {
		return focus_id;
	}
	public void setFocus_id(String focus_id) {
		this.focus_id = focus_id;
	}
	public String getFocus() {
		return focus;
	}
	public void setFocus(String focus) {
		this.focus = focus;
	}
	public String getBefocus() {
		return befocus;
	}
	public void setBefocus(String befocus) {
		this.befocus = befocus;
	}
	
	
}
